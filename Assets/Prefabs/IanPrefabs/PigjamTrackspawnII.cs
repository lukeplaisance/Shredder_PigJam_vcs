using UnityEngine;

public class PigjamTrackspawnII : MonoBehaviour
{
    public Transform TrackSpawnPoint;
    public GameObject TrackBlock;

    void Start()
    {
        Instantiate(TrackBlock, TrackSpawnPoint.position);
        Instantiate(TrackBlock, TrackSpawnPoint.position + Vector3.back * 10f);
        Instantiate(TrackBlock, TrackSpawnPoint.position + Vector3.back * 20f);

    }
    void Update()
    {
        if(FindAnyObjectByType(Level_Block).transform.position.z < -10f)
        {
            Destroy(GameObject);
            Instantiate(TrackBlock, TrackSpawnPoint.position);
        }
    }

    
}
